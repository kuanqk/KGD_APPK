class Command(BaseCommand):
    help = "Проставить admin_comment и status для обращений 64–71."

    def add_arguments(self, parser):
        parser.add_argument(
            "--dry-run",
            action="store_true",
            help="Только показать, без записи в БД.",
        )

    def handle(self, *args, **options):
        dry = options["dry_run"]
        now = timezone.now()
        for pk, status, comment in RESPONSES:
            exists = Feedback.objects.filter(pk=pk).exists()
            if not exists:
                self.stdout.write(self.style.WARNING(f"  #{pk}: нет записи, пропуск"))
                continue
            if dry:
                self.stdout.write(f"  #{pk} [{status}] ({len(comment)} симв.)")
                continue
            Feedback.objects.filter(pk=pk).update(
                status=status,
                admin_comment=comment,
                is_reviewed=True,
                resolved_at=None if status == FeedbackStatus.IN_PROGRESS else now,
            )
            self.stdout.write(self.style.SUCCESS(f"  #{pk}: обновлено"))

        if dry:
            self.stdout.write(self.style.NOTICE("Dry-run: изменения не записаны."))
        else:
            self.stdout.write(self.style.NOTICE("Готово."))
